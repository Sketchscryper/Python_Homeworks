# Способ 1: импорт с псевдонимом
import utils as u

# Использование функций через псевдоним
print("Квадрат числа через псевдоним:", u.square(4)) # 16
print("Куб числа через псевдоним:", u.cube(3)) # 27
print("Степень числа через псевдоним:", u.power(3, 4)) # 81

# Способ 2: импорт всех функций
from utils import *

# Использование функций напрямую
print("Квадрат числа напрямую:", square(5)) # 25
print("Куб числа напрямую:", cube(2)) # 8
print("Степень напрямую:", power(2, 3)) # 8

# Важно понимать, что использование "from utils import *" может быть нежелательным по следующим причинам:

# 1. Затрудняет понимание кода
#   - Сложно определить, какие функции и переменные импортированы из какого модуля
#   - Ухудшает читаемость кода

# 2. Проблемы с конфликтами имен
#   - Если в разных модулях есть функции с одинаковыми именами, может возникнуть конфликт
#   - Сложно отследить, какая именно функция используется в конкретном месте

# 3. Производительность
#   - Импортирует все содержимое модуля, даже если используются только несколько функций
#   - Может привести к ненужному расходу памяти

# 4. Сложности с поддержкой
#   - При изменении содержимого модуля могут возникнуть неожиданные ошибки
#   - Трудно отследить зависимости между модулями

# Рекомендуется использовать:
# - явный импорт конкретных функций: "from module import function_name"
# - импорт всего модуля с псевдонимом: "import module as m"
